import random
# 题目：输出一个随机数。

# 生成一个10~20之间的随机数
print("生成一个10~20之间的随机数:", random.uniform(10, 20))

# 生成一个10~20之间的随机整数
print("生成一个10~20之间的随机整数:", random.randint(10, 20))

# 输入0-1之间的随机数
print("0-1之间的随机数:", random.random())
